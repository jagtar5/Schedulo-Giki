    dem = _lab_demand_by_faculty[fac]
    cap = _lab_capacity_by_faculty.get(fac, 0)
    if cap >= dem:
        continue
    deficit = dem - cap
    labs_needed = (deficit + slots_per_week - 1) // slots_per_week
    # continue room id sequence after any lecture autos
    existing_nums = []
    for r in rooms:
        rid = str(r.get("room_id", ""))
        m = _re.match(r"^R(\d+)$", rid)
        if m:
            existing_nums.append(int(m.group(1)))
    next_num = (max(existing_nums) + 1) if existing_nums else 1
    for i in range(labs_needed):
        rooms.append(
            {
                "room_id": f"R{next_num + i:03d}",
                "name": f"{fac} Lab Auto {i+1}",
                "faculty": fac,
                "capacity": 150,
                "room_type": "Lab",
            }
        )
    _lab_capacity_by_faculty[fac] = cap + labs_needed * slots_per_week